
public class example2_3 {

	public static void main(String[] args) {
		//예제 02_03
		
		System.out.println(5+3);		// 화면에 5+3 의 결과인 8이 출력된다.
		System.out.println(5-3);		// 화면에 5-3 의 결과인 2이 출력된다.
		System.out.println(5*3);		// 화면에 5*3 의 결과인 15이 출력된다.
		System.out.println(5/3);		// 화면에 5/3 의 결과인 1이 출력된다.
	}

}
